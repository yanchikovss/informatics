import matplotlib.pyplot as plt 
import numpy as np 
fig=plt.figure() 
x = np.linspace(-100, 0, 1000) 
y1=((x**1)+(1**1))/x**1 
y2=((x**(1))+(2**(1)))/x**(1) 
y3=((x**(2))+(1**(2)))/x**(2) 
plt.plot(x, y3, 'g') 
plt.plot(x, y2, color='orange') 
plt.plot(x, y1, 'b') 
plt.legend(['a=1,b=2', 'a=2,b=1','a=1,b=1']) 
plt.xlabel('ось x') 
plt.ylabel('ось y') 
plt.title('график1') 
